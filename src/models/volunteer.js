class Volunteer {
  constructor(
    id,
    förnamn,
    efternamn,
    telefon,
    email,
    address,
    beskrivning,
    körkort,
    bil,
    mat,
    varor,
    ärenden,
    djur,
    prata,
    myndigheter,
    teknik,
    grupp,
    datum,
    status
  ) {
    this.id = id;
    this.förnamn = förnamn;
    this.efternamn = efternamn;
    this.telefon = telefon;
    this.email = email;
    this.address = address;
    this.beskrivning = beskrivning;
    this.körkort = körkort;
    this.bil = bil;
    this.mat = mat;
    this.varor = varor;
    this.ärenden = ärenden;
    this.djur = djur;
    this.prata = prata;
    this.myndigheter = myndigheter;
    this.teknik = teknik;
    this.grupp = grupp;
    this.datum = datum;
    this.status = status;
  }
}

export default Volunteer;
